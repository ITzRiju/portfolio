/**
 * Calculate percentage change
 */
export function calculatePercentageChange(current: number, previous: number): number {
  if (previous === 0) return current > 0 ? 100 : 0;
  return ((current - previous) / previous) * 100;
}

/**
 * Format percentage
 */
export function formatPercentage(value: number): string {
  return `${value >= 0 ? '+' : ''}${value.toFixed(1)}%`;
}

/**
 * Get initials from name
 */
export function getInitials(name: string): string {
  return name
    .split(' ')
    .map(word => word.charAt(0).toUpperCase())
    .join('')
    .slice(0, 2);
}

/**
 * Check if current time is within business hours
 */
export function isWithinBusinessHours(): boolean {
  const now = new Date();
  const currentHour = now.getHours();
  const currentDay = now.getDay(); // 0 = Sunday, 6 = Saturday
  
  // Assuming business hours: Monday-Friday 9 AM - 6 PM, Saturday 10 AM - 4 PM
  if (currentDay === 0) return false; // Sunday closed
  if (currentDay >= 1 && currentDay <= 5) {
    return currentHour >= 9 && currentHour < 18; // Monday-Friday
  }
  if (currentDay === 6) {
    return currentHour >= 10 && currentHour < 16; // Saturday
  }
  return false;
}

/**
 * Generate color from string (for avatars)
 */
export function generateColorFromString(str: string): string {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  const hue = hash % 360;
  return `hsl(${hue}, 70%, 50%)`;
}